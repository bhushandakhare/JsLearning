console.log(`=========== 5 to 15 by incrementing 1 ==========`);
var result="";
for (let index = 5; index <=15; index=index+1){
result=result+index+ " ";
   
}
console.log(result);
console.log(`=========== 50 to 40 by decrementing 1 ==========`);
var result="";
for (let index = 50; index >=40; index--){
result=result+index+ " ";
   
}
console.log(result);
